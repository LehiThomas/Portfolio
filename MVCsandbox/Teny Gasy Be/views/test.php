<p><strong>Compass</strong> test minimum scores are the following: Reading 72; Algebra 22.<br /><br /> <strong>Students submitting 15+ college level credits</strong> instead of a high school transcript should generally have a cumulative GPA of 2.5+ on their college work. Remedial/non-college level and technical courses do not count toward the 15 credit level.&nbsp;</p>
<p>If 24 or more semester credits (36 quarter hours) have been earned by the application deadline, no ACT, SAT, or secondary test will be required. These applicants should apply as Transfer students with more than 24 credit hours (36 quarter hours) earned.</p>