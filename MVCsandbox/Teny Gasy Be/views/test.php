<p>BYU-Idaho places a great deal of importance on graduating from high school or completing the equivalent. <br /><br /><strong>If the student will graduate from high school or complete a home school program:</strong> Is the program accredited?</p>
<p><strong>Graduating from an accredited program: </strong>Students graduating from a state-accredited or regionally-accredited high school program in the United States, should submit high school transcripts and ACT or SAT scores. Information about a school's accreditation level is often listed on the high school transcript, or may be available from the high school counseling office or school district office. Approved regional accrediting bodies are:</p>
<ul>
    <li>Middle States Association of Colleges and Schools Commission on Higher Education</li>
    <li>New England Association of Schools and Colleges Commission on Higher Education</li>
    <li>North Central Association of Colleges and Schools Commission of Higher Education</li>
    <li>Northwest Commission on Colleges and Universities (NWCC)</li>
    <li>Northwest Accreditation Commission (NWAC)</li>
    <li>Southern Association of Colleges and Schools Commission on Colleges</li>
    <li>Western Association of Schools and College Accrediting Commission for Senior Colleges and Universities</li>
</ul>
<p>Students from schools accredited by private accreditation agencies should follow the 'not accredited' instructions below.<br /><br /><strong>Graduating from/completing a non-accredited program: </strong>Students whose secondary education program is not accredited (including those whose program is accredited through a non-state or regional accreditation agency) must submit ACT or SAT scores along with one of the following approved options:</p>
<ul>
    <li>GED (<a href="http://www.gedtestingservice.com">www.gedtestingservice.com</a>)</li>
    <li>HiSet (<a href="http://www.ets.org/hiset">www.ets.org/hiset</a>)</li>
    <li>TASC Test (<a href="http://www.tasctest.com">www.tasctest.com</a>)</li>
    <li>California High School Proficiency Exam (<a href="http://www.chspe.net">www.chspe.net</a>)</li>
    <li>ACT Compass Test (<a href="http://www.act.org/compass">www.act.org/compass</a>)</li>
    <li>15+ completed semester college credits</li>
</ul>
<p><strong>Not graduating/completing a high school or home school program:&nbsp;</strong>Students who will not graduate from or complete any type of program may submit one of the following items in the in addition to their ACT/SAT scores.</p>
<ul>
    <li>GED (<a href="http://www.gedtestingservice.com">www.gedtestingservice.com</a>)</li>
    <li>HiSet (<a href="http://www.ets.org/hiset">www.ets.org/hiset</a>)</li>
    <li>TASC Test (<a href="http://www.tasctest.com">www.tasctest.com</a>)</li>
    <li>California High School Proficiency Exam (<a href="http://www.chspe.net">www.chspe.net</a>)</li>
</ul>
